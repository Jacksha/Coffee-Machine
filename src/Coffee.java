public class Coffee {
    String coffeeName;
    int watter;
    int milk;
    int beans;
    int cups;
    int money;

    Coffee(String coffeeName, int watter, int milk, int beans, int cups, int money) {
        this.coffeeName = coffeeName;
        this.watter = watter;
        this.milk = milk;
        this.beans = beans;
        this.cups = cups;
        this.money = money;
    }
}